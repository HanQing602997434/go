/*
	windows下开发步骤：
		1.安装vscode

		2.go文件以.go

		3.go build编译.go文件，生成.exe文件

		4.go run运行源码，速度慢

	go程序开发注意事项：
		1.go源文件以"go"为扩展名

		2.go应用程序的执行入口是main()函数

		3.go语言严格区分大小写

		4.go方法由一条条语句构成，每个语句后不需要分号（go语言会在每行后自动加分号），
		这也体现了go的简洁性

		5.go编译器是一行行进行编译的，因此我们一行就写一条语句，不能把多条语句写在同
		一行，否则报错

		6.go语言定义的变量或者import的包如果没有使用到，代码编译不能通过

		7.大括号都是成对出现，缺一不可
*/